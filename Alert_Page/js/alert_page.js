function page_logic() {
    $('#show_font div input').click(function () {
        if (this.type == "checkbox") {
            if (document.getElementById(this.id).value == "on") {
                document.getElementById('div' + this.id.substring(3, this.id.length)).className =
                    "textbox_check_on_div";
                document.getElementById('A11' + this.id.substring(3, this.id.length)).value = "off";
            } else {
                document.getElementById('div' + this.id.substring(3, this.id.length)).className =
                    "textbox_check_off_div";
                document.getElementById('A11' + this.id.substring(3, this.id.length)).value = "on";
            }
        }
    });
    $('div[name^="oval"]').click(function () {
        if (document.getElementById('A11' + this.id.substring(3, this.id.length)).value == "off") {
            document.getElementById('div' + this.id.substring(3, this.id.length)).className = "oval_off_div";
            document.getElementById('A11' + this.id.substring(3, this.id.length)).value = "on";
        } else {
            document.getElementById('div' + this.id.substring(3, this.id.length)).className = "oval_on_div";
            document.getElementById('A11' + this.id.substring(3, this.id.length)).value = "off";
        }
    });
    $('div[name^="circle"]').click(function () {
        if (this.id != "div_1_1" && this.id != "div_1_2") {
            //多選
            if (document.getElementById('A11' + this.id.substring(3, this.id.length)).value == "off") {
                document.getElementById('div' + this.id.substring(3, this.id.length)).className = "circle_off_div";
                document.getElementById('A11' + this.id.substring(3, this.id.length)).value = "on";
            } else {
                document.getElementById('div' + this.id.substring(3, this.id.length)).className = "circle_on_div";
                document.getElementById('A11' + this.id.substring(3, this.id.length)).value = "off";
            }
        } else {
            //單選
            if (document.getElementById('A11' + this.id.substring(3, this.id.length)).value == "off") {
                document.getElementById('div' + this.id.substring(3, this.id.length)).className = "circle_off_div";
                document.getElementById('A11' + this.id.substring(3, this.id.length)).value = "on";
                document.getElementById("A11" + this.id.substring(3, this.id.length - 2)).value = "";
            } else {
                document.getElementById('div' + this.id.substring(3, this.id.length)).className = "circle_on_div";
                document.getElementById('A11' + this.id.substring(3, this.id.length)).value = "off";
                if (this.id.substring(this.id.length - 1, this.id.length) == "1") {
                    document.getElementById('div' + this.id.substring(3, this.id.length - 1) + "2").className = "circle_off_div";
                    document.getElementById('A11' + this.id.substring(3, this.id.length - 1) + "2").value = "on";
                    document.getElementById("A11" + this.id.substring(3, this.id.length - 2)).value = "1";
                }
                else {
                    document.getElementById('div' + this.id.substring(3, this.id.length - 1) + "1").className = "circle_off_div";
                    document.getElementById('A11' + this.id.substring(3, this.id.length - 1) + "1").value = "on";
                    document.getElementById("A11" + this.id.substring(3, this.id.length - 2)).value = "2";
                }
            }
        }
    });
    $('div[name^="radio"]').click(function () {
        if (document.getElementById('A11' + this.id.substring(3, this.id.length)).value == "off") {
            document.getElementById('div' + this.id.substring(3, this.id.length)).className = "textbox_radio_off_div";
            document.getElementById('A11' + this.id.substring(3, this.id.length)).value = "on";
            document.getElementById("A11" + this.id.substring(3, this.id.length - 2)).value = "";
        } else {
            document.getElementById('div' + this.id.substring(3, this.id.length)).className = "textbox_radio_on_div";
            document.getElementById('A11' + this.id.substring(3, this.id.length)).value = "off";
            if (this.id.substring(this.id.length - 1, this.id.length) == "1") {
                document.getElementById('div' + this.id.substring(3, this.id.length - 1) + "2").className = "textbox_radio_off_div";
                document.getElementById('A11' + this.id.substring(3, this.id.length - 1) + "2").value = "on";
                document.getElementById("A11" + this.id.substring(3, this.id.length - 2)).value = "1";
            }
            else {
                document.getElementById('div' + this.id.substring(3, this.id.length - 1) + "1").className = "textbox_radio_off_div";
                document.getElementById('A11' + this.id.substring(3, this.id.length - 1) + "1").value = "on";
                document.getElementById("A11" + this.id.substring(3, this.id.length - 2)).value = "2";
            }
        }
    });
    $('div[name^="6_radio"]').click(function () {
        if (document.getElementById("A11" + this.id.substring(3, this.id.length - 2)).value == this.id.substring(this.id.length - 1, this.id.length)) {
            document.getElementById("A11" + this.id.substring(3, this.id.length - 2)).value = "";
            document.getElementById(this.id).className = "";
        } else {
            document.getElementById("A11" + this.id.substring(3, this.id.length - 2)).value = this.id.substring(this.id.length - 1, this.id.length);
            for (var i = 1; i < 7; i++) {
                if (i == this.id.substring(this.id.length - 1, this.id.length)) {
                    document.getElementById(this.id).className = "circle_on_div";
                } else {
                    document.getElementById("div" + this.id.substring(3, this.id.length - 2) + "_" + i).className = "";
                }
            }
        }
    });
}
