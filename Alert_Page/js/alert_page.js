function LOGIC_alert_Message_S_EMP(input, input2) {
    $('div[name^="btn_s_item"]').click(function () {
        document.getElementById('buf_emp_code').style.color = "blue";
        document.getElementById('buf_emp_code').value = input[this.id.split('_')[1]].ITEM_KEY;
        document.getElementById('buf_emp_name').value = input[this.id.split('_')[1]].ITEM_NAME;
        for (i = 1; i < input.length; i++) {
            if ('item_' + i == this.id) {
                document.getElementById('item_' + i).className = "btn_s_item_check";
            } else {
                document.getElementById('item_' + i).className = "btn_s_item";
            }
        }
    });
    document.getElementById('btn_s_a').addEventListener('click', function () {
        if (document.getElementById('buf_emp_name').value == "") {
            document.getElementById(input2).value = "";
            document.getElementById(input2.substring(0, input2.length - 1) + "2").value = "";
        } else {
            document.getElementById(input2).value = document.getElementById('buf_emp_name').value;
            document.getElementById(input2.substring(0, input2.length - 1) + "2").value = document.getElementById('buf_emp_code').value;
        }
        document.getElementById(input2).value = document.getElementById('buf_emp_name').value;
        document.getElementById('model').className = "model_hidden";
        document.getElementById('ALERT_M').style.top = '-200%';
        document.getElementById('ALERT_M').innerHTML = "";
    });
    document.getElementById('btn_s_c').addEventListener('click', function () {
        document.getElementById('model').className = "model_hidden";
        document.getElementById('ALERT_M').style.top = '-200%';
        document.getElementById('ALERT_M').innerHTML = "";
    });
    document.getElementById('buf_emp_code').onblur = function () {
        var buf = document.getElementById('buf_emp_code').value;
        var ans_key = false;
        if (!isNaN(buf)) {
            for (i = 0; i < input.length; i++) {
                if (input[i].ITEM_KEY.toString() == buf.toString()) {
                    document.getElementById('buf_emp_code').style.color = "green";
                    document.getElementById('buf_emp_name').value = input[i].ITEM_NAME;
                    document.getElementById('item_' + i).className = "btn_s_item_check";
                    ans_key = true;
                } else {
                    if (i != 0) {
                        document.getElementById('item_' + i).className = "btn_s_item";
                    }
                }
            }
            if (!ans_key) {
                document.getElementById('buf_emp_code').style.color = "red";
                document.getElementById('buf_emp_name').value = "";
            }
        } else {
            document.getElementById('buf_emp_code').style.color = "red";
            document.getElementById('buf_emp_name').value = "";
        }
    };
}
